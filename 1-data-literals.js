// Data literals are them most basic form of data that
// you can work with when programming

// An integer is a whole number.
// integers in javascript are of the "number" type
console.log(5, " is an integer.")
console.log(typeof 5)
console.log(10 + 5)

// Float is a decimal point number.
// Be careful as they are inaccurate sometimes.
// JavaScript reads both integer and floats type
// as number
console.log(21.4, "is a float.")
console.log(typeof 21.4)
console.log(21.4 + 13.6)
console.log(0.1 + 0.2)
console.log(21.4 + 10)

// String is text. If you try to add
// text to numbers it will convert the
// numbers to text. 
console.log("Devin", "is a string.")
console.log(typeof "Devin")
console.log("Devin" + " Jackson")
console.log("Devin" + 10)
console.log(10 + "Devin")

// Boolean is a flag that helps say
// whether something is true or not.
// We'll see in later lessons how to use them
console.log(true, false)